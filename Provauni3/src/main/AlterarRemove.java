    private void removeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeActionPerformed
        
        if(tabelaAlunos.getSelectedRow() != -1){
            tabelmodel.removeRow(tabelaAlunos.getSelectedRow(),(String) tabelaAlunos.getModel().getValueAt(tabelaAlunos.getSelectedRow(), 1));
        }
        
        
    }//GEN-LAST:event_removeActionPerformed

    private void alterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_alterarActionPerformed
        
        if(tabelaAlunos.getSelectedRow() != -1){
            Aluno aAltear = new Aluno((Integer)tabelaAlunos.getModel().getValueAt(tabelaAlunos.getSelectedRow(),0 ),(String) tabelaAlunos.getModel().getValueAt(tabelaAlunos.getSelectedRow(),1 ) , (String)tabelaAlunos.getModel().getValueAt(tabelaAlunos.getSelectedRow(),2 ));
            Alterar view = new Alterar(aAltear);
            view.setVisible(true);
            this.setVisible(false);
        }
        
        
    }//GEN-LAST:event_alterarActionPerformed

